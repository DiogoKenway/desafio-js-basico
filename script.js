function somar(numero1, numero2) {
    const resultado = numero1 + numero2
    return `A Soma dos Dois Números é: ${resultado}`
}

function subtrair(numero1, numero2) {
    const resultado = numero1 - numero2
    return `O Resulta da subtração é: ${resultado}`
}

function multiplicação(numero1, numero2) {
    const resultado = numero1 * numero2
    return `O Resultado da multiplicação é: ${resultado}`
}

function divisao(numero1, numero2) {
    const resultado = numero1 / numero2
    return `O Resultado da divisão é: ${resultado}`
}


console.log(somar(5, 4))
console.log(subtrair(5, 4))
console.log(multiplicação(5, 4))
console.log(divisao(5, 4))